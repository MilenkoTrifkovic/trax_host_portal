/// Font weight enum based on Figma design specifications
/// Maps Figma font weight names to standardized enum values
enum FigmaFontWeight {
  /// Figma: Thin (100)
  thin,

  /// Figma: Extra Light (200)
  extraLight,

  /// Figma: Light (300)
  light,

  /// Figma: Regular (400)
  regular,

  /// Figma: Medium (500)
  medium,

  /// Figma: Semi Bold (600)
  semiBold,

  /// Figma: Bold (700)
  bold,

  /// Figma: Extra Bold (800)
  extraBold,

  /// Figma: Black (900)
  black,
}
